function buildApprovalRulesTab(module, existing) {
  const name = module?.name || '单据';
  const source = `${name}审批设置`;
  if (existing) return { ...existing, label: '审批规则' };
  const rows = [
    {
      title: `${name}修改审批流程`,
      sub: `${name}已提交、已审批或已生效后，如果修改关键字段，按指定审批流处理。流程来源：${source}。`,
      type: 'select',
      value: `${name}修改审批流程`,
      options: ['不走审批', `${name}修改审批流程`, `${name}关键字段修改审批流程`, '主管审批流程'],
    },
  ];
  if (module?.code !== 'stock') {
    rows.push({
      title: `${name}终止审批流程`,
      sub: `${name}提前终止、异常关闭、作废或撤回时，必须填写原因并按指定审批流处理。流程来源：${source}。`,
      type: 'select',
      value: `${name}终止审批流程`,
      options: ['不允许终止', `${name}终止审批流程`, `${name}作废审批流程`, '主管审批流程'],
    });
  }
  return {
    k: 'approval',
    label: '审批规则',
    rows,
  };
}

function normalizePolicyConfig(config, module) {
  const approvalTab = config.tabs.find(t => t.k === 'approval' || t.k === 'approvalRules');
  const otherTabs = config.tabs.filter(t => t !== approvalTab);
  return {
    ...config,
    tabs: [...otherTabs, buildApprovalRulesTab(module, approvalTab)],
  };
}

function PolicyConfigScreen({ config, module }) {
  const normalized = normalizePolicyConfig(config, module);
  const [tab, setTab] = useState(normalized.tabs[0].k);
  const current = normalized.tabs.find(x => x.k === tab) || normalized.tabs[0];

  return (
    <Card>
      <div style={{display:'flex',alignItems:'baseline',gap:10,marginBottom:12}}>
        <div style={{fontSize:16,fontWeight:700}}>{normalized.title}</div>
        <div style={{fontSize:12,color:'var(--aw-fg-3)'}}>用于控制业务逻辑、审批触发、转单回写和异常拦截规则</div>
      </div>
      <Tabs items={normalized.tabs.map(({k,label}) => ({k,label}))} active={tab} onChange={setTab} />
      <div style={{marginTop:14}}>
        {current.rows.map((row, idx) => <PolicyRuleRow key={`${tab}-${idx}`} row={row} />)}
      </div>
      <div style={{display:'flex',justifyContent:'flex-end',alignItems:'center',gap:8,marginTop:16,paddingTop:12,borderTop:'1px solid var(--aw-divider)'}}>
        <div style={{display:'flex',gap:8}}>
          <Btn kind="secondary">重置默认</Btn>
          <Btn kind="primary">保存</Btn>
        </div>
      </div>
    </Card>
  );
}

function PolicyRuleRow({ row }) {
  const [on, setOn] = useState(row.defaultOn !== false);
  const [value, setValue] = useState(row.value || row.options?.[0] || '');
  const [percent, setPercent] = useState(row.percent || '10%');
  const [date, setDate] = useState(row.date || '');
  return (
    <div style={{padding:'15px 18px',border:'1px solid var(--aw-border)',borderRadius:6,display:'grid',gridTemplateColumns:'minmax(0,1fr) 260px',gap:18,alignItems:'center',marginBottom:10,background:'#fff'}}>
      <div>
        <div style={{display:'flex',alignItems:'center',gap:8,marginBottom:5}}>
          <span style={{fontSize:14,fontWeight:600,color:'var(--aw-fg-1)'}}>{row.title}</span>
          {row.tag && <span className="aw-state aw-state-y">{row.tag}</span>}
        </div>
        <div style={{fontSize:12,color:'var(--aw-fg-3)',lineHeight:1.65}}>{row.sub}</div>
      </div>
      <div style={{display:'flex',justifyContent:'flex-end'}}>
        {row.type === 'percentLimit' ? (
          <div style={{display:'flex',gap:8,alignItems:'center',justifyContent:'flex-end'}}>
            <Select value={value} onChange={e => setValue(e.target.value)} style={{width:136}}>
              {row.options.map(opt => <option key={opt}>{opt}</option>)}
            </Select>
            <Input value={percent} onChange={e => setPercent(e.target.value)} disabled={value === '不允许超派'} placeholder="超出比例" style={{width:92}} />
          </div>
        ) : row.type === 'dateRelease' ? (
          <div style={{display:'flex',gap:8,alignItems:'center',justifyContent:'flex-end'}}>
            <Select value={value} onChange={e => setValue(e.target.value)} style={{width:150}}>
              {row.options.map(opt => <option key={opt}>{opt}</option>)}
            </Select>
            <Input type="date" value={date} onChange={e => setDate(e.target.value)} style={{width:120}} />
          </div>
        ) : row.type === 'select' ? (
          <Select value={value} onChange={e => setValue(e.target.value)} style={{width:240}}>
            {row.options.map(opt => <option key={opt}>{opt}</option>)}
          </Select>
        ) : (
          <div>
            <Radio on={on} onClick={() => setOn(true)}>启用</Radio>
            <Radio on={!on} onClick={() => setOn(false)}>关闭</Radio>
          </div>
        )}
      </div>
    </div>
  );
}

