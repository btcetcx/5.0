const { useState } = React;

function PolicyScreen({ module: mod = MODULE_DOC }) {
  const m = mod || MODULE_DOC;
  if (['doc', 'proj', 'prod', 'mat', 'proc', 'craft', 'bom'].includes(m.code)) return <RndPolicyScreen module={m} />;
  if (['supp', 'pr', 'inq', 'ord'].includes(m.code)) return <PurchasePolicyScreen module={m} />;
  if (['cust', 'splan', 'quote', 'cont', 'sord', 'sret', 'sexch'].includes(m.code)) return <SalesPolicyScreen module={m} />;
  if (['stock', 'out', 'in', 'trans', 'check', 'wloc', 'oqc', 'iqc'].includes(m.code)) return <WarehousePolicyScreen module={m} />;
  if (['mdem', 'mplan', 'mord', 'mwo', 'msch', 'mout'].includes(m.code)) return <ManufacturingPolicyScreen module={m} />;
  const [tab, setTab] = useState('version');

  // 版本控制策略 — 前 3 条默认「是」，第 4 条默认「否」
  const [v1, setV1] = useState('y'); // 新版本是否强制审批
  const [v2, setV2] = useState('y'); // 旧版本是否自动归档
  const [v3, setV3] = useState('y'); // 编辑是否产生新版本
  const [v4, setV4] = useState('n'); // 版本号是否自动递增

  // 访问权限策略 — 全部默认「否」
  const [a1, setA1] = useState('n'); // 非本部门人员是否可查看
  const [a2, setA2] = useState('n'); // 草稿状态是否对外可见
  const [a3, setA3] = useState('n'); // 已停用文档是否保留查看权

  const Row = ({ title, sub, val, set }) => (
    <div style={{ padding: '16px 18px', border: '1px solid var(--aw-border)', borderRadius: '6px', display: 'flex', alignItems: 'center', justifyContent: 'space-between', marginBottom: '10px' }}>
      <div>
        <div style={{ fontSize: '14px', fontWeight: 600, marginBottom: '4px' }}>{title}</div>
        <div style={{ fontSize: '12px', color: 'var(--aw-fg-3)' }}>{sub}</div>
      </div>
      <div>
        <Radio on={val === 'y'} onClick={() => set('y')}>是</Radio>
        <Radio on={val === 'n'} onClick={() => set('n')}>否</Radio>
      </div>
    </div>
  );

  return (
    <Card>
      <Tabs
        items={[
          { k: 'version', label: '版本控制策略' },
          { k: 'access', label: '访问权限策略' },
        ]}
        active={tab}
        onChange={setTab}
      />

      {tab === 'version' && (
        <>
          <Row title="新版本是否强制审批" sub="新增版本是否必须经过审批流程" val={v1} set={setV1} />
          <Row title="旧版本是否自动归档" sub="新版本生效后旧版本自动归档" val={v2} set={setV2} />
          <Row title="编辑是否产生新版本" sub="编辑已生效文档自动生成新版本号" val={v3} set={setV3} />
          <Row title="版本号是否自动递增" sub="系统自动按规则递增版本号" val={v4} set={setV4} />
        </>
      )}

      {tab === 'access' && (
        <>
          <Row title="非本部门人员是否可查看" sub="允许其他部门人员查阅本部门文档" val={a1} set={setA1} />
          <Row title="草稿状态是否对外可见" sub="草稿状态的文档对非编辑者可见" val={a2} set={setA2} />
          <Row title="已停用文档是否保留查看权" sub="已停用的文档仍可被原授权人员查看" val={a3} set={setA3} />
        </>
      )}

      <div style={{ display: 'flex', justifyContent: 'flex-end', gap: '8px', marginTop: '16px' }}>
        <Btn kind="secondary">重置默认</Btn>
        <Btn kind="primary">保存</Btn>
      </div>
    </Card>
  );
}

