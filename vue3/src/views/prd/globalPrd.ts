function treePage(docId: string, label: string, sectionIds: PrdSectionId[], level: PrdTreeNode['level'] = 2): PrdTreeNode {
  const childLevel = Math.min(level + 1, 4) as PrdTreeNode['level'];
  return {
    id: `global-page-${docId}`,
    label,
    level,
    docId,
    children: prdSections.filter((section) => sectionIds.includes(section.id)).map((section) => ({
      id: `global-${docId}-${section.id}`,
      label: section.id === 'flows' ? '业务流程图' : section.label,
      level: childLevel,
      docId,
      sectionId: section.id,
    })),
  };
}

