function buildQuestionLocalHistory(q, savedItems) {
  const items = [];
  if (q.answer) {
    items.push({
      status:q.resolved ? '已处理' : q.followup ? '待追问' : '待确认',
      tone:q.resolved ? 'g' : q.followup ? 'b' : 'y',
      note:q.answer,
    });
  }
  (savedItems || []).forEach(item => {
    items.push({
      status:item.method,
      tone:item.method === '业务待确认' ? 'b' : 'y',
      note:item.content || `${item.priority}优先级页面反馈`,
    });
  });
  return items;
}

